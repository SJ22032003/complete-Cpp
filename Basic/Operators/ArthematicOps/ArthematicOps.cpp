#include <iostream>
#include <cmath>
using namespace std;
int main(int argc, char const *argv[])
{
	/* Arthematic Operators */

	cout << 70 + 55 << endl;

	cout << 70 - 55 << endl;

	cout << 70 * 55 << endl;

	cout << 70 / 55 << endl; // int value , so no decimal

	cout << 70 % 55 << endl; // it gives remainder

	cout << pow(2,2) << endl; // using math library

	return 0;
}