#include <iostream>

using namespace std;
int main(int argc, char const *argv[])
{
	
	int i;
	cin>>i;
	cout<<"this is an Integer = "<<i<<endl;

	float f;
	cin>>f;
	cout<<"this is float = "<<f<<endl;

	double d;
	cin>>d;
	cout<<"this is double = "<<d<<endl;

	char c;
	cin>>c;
	cout<<"this is character = "<<c<<endl;

	long l;
	cin>>l;
	cout<<"this is a long = "<<l<<endl;

	return 0;	
}